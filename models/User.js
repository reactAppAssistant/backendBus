const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Remove the password hashing middleware
// No need to define any methods related to password matching

const User = mongoose.model('User', userSchema);

module.exports = User;
